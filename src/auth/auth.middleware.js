import jwt from 'jsonwebtoken';
import { logInfo, logWarn, logError } from '../utils/logger.js';
import { normalizeRole } from '../middleware/rbac.middleware.js';

// JWT secret key - In production, use environment variable
const JWT_SECRET = process.env.JWT_SECRET || 'demo-secret-key-change-in-production';

/**
 * Valid roles for client authentication
 */
export const ROLES = {
  KIOSK: 'KIOSK',
  MONITOR: 'MONITOR'
};

const APP_MONITOR_ROLES = new Set(['SUPER_ADMIN', 'DIVISION_ADMIN', 'MONITOR']);
const APP_KIOSK_ROLES = new Set(['USER']);

/**
 * Middleware to authenticate Socket.IO connections using JWT
 * 
 * This middleware validates JWT tokens and attaches role and clientId
 * to the socket context. Connections without valid tokens are rejected.
 * 
 * Architecture Note: This backend is view-only and does NOT process
 * video streams. It only handles signaling and event broadcasting.
 * 
 * @param {Object} socket - Socket.IO socket instance
 * @param {Function} next - Next middleware function
 */
export const authenticateSocket = (socket, next) => {
  try {
    // Extract token from handshake auth
    const token = socket.handshake.auth?.token || socket.handshake.headers?.authorization?.replace('Bearer ', '');

    if (!token) {
      logWarn('Auth', 'Authentication failed: No token provided', { 
        socketId: socket.id,
        ip: socket.handshake.address 
      });
      return next(new Error('Authentication error: No token provided'));
    }

    // Verify JWT token
    const decoded = jwt.verify(token, JWT_SECRET);

    // Application JWT: userId + role -> map to socket MONITOR | KIOSK (do not rename events)
    const appUserId = decoded.id || decoded.userId;
    const appRole = normalizeRole(decoded.role);

    if (appUserId && (APP_MONITOR_ROLES.has(appRole) || APP_KIOSK_ROLES.has(appRole))) {
      const socketRole = APP_MONITOR_ROLES.has(appRole) ? ROLES.MONITOR : ROLES.KIOSK;
      const clientId = decoded.user_id || appUserId;
      socket.data.role = socketRole;
      socket.data.clientId = String(clientId);
      socket.data.userId = appUserId;
      socket.data.user = {
        userId: appUserId,
        role: appRole,
        division_id: decoded.division_id || null,
        name: decoded.name ?? null,
      };
      logInfo('Auth', 'Client authenticated (app JWT)', {
        clientId: socket.data.clientId,
        appRole,
        socketRole,
        socketId: socket.id,
        ip: socket.handshake.address
      });
      return next();
    }

    // Legacy JWT: clientId + role KIOSK | MONITOR
    if (!decoded.role || (decoded.role !== ROLES.KIOSK && decoded.role !== ROLES.MONITOR)) {
      logWarn('Auth', 'Authentication failed: Invalid role', { 
        socketId: socket.id,
        role: decoded.role,
        ip: socket.handshake.address 
      });
      return next(new Error('Authentication error: Invalid role'));
    }
    if (!decoded.clientId) {
      logWarn('Auth', 'Authentication failed: Missing clientId', { 
        socketId: socket.id,
        ip: socket.handshake.address 
      });
      return next(new Error('Authentication error: Missing clientId'));
    }

    socket.data.role = decoded.role;
    socket.data.clientId = decoded.clientId;
    socket.data.userId = decoded.userId || decoded.clientId;
    socket.data.user = null;

    logInfo('Auth', 'Client authenticated (legacy JWT)', {
      clientId: decoded.clientId,
      role: decoded.role,
      socketId: socket.id,
      ip: socket.handshake.address
    });

    next();
  } catch (error) {
    if (error.name === 'JsonWebTokenError') {
      logWarn('Auth', 'Authentication failed: Invalid token', { 
        socketId: socket.id,
        ip: socket.handshake.address,
        error: error.message 
      });
      return next(new Error('Authentication error: Invalid token'));
    }
    if (error.name === 'TokenExpiredError') {
      logWarn('Auth', 'Authentication failed: Token expired', { 
        socketId: socket.id,
        ip: socket.handshake.address 
      });
      return next(new Error('Authentication error: Token expired'));
    }
    logError('Auth', 'Authentication error', { 
      socketId: socket.id,
      ip: socket.handshake.address,
      error: error.message 
    });
    return next(new Error('Authentication error: ' + error.message));
  }
};

/**
 * Helper function to generate JWT token for testing/demo purposes
 * In production, tokens should be generated by a separate auth service
 * 
 * @param {string} clientId - Unique client identifier
 * @param {string} role - Client role (KIOSK or MONITOR)
 * @param {number} expiresIn - Token expiration time in seconds (default: 24 hours)
 * @returns {string} JWT token
 */
export const generateToken = (clientId, role, expiresIn = 86400) => {
  if (!clientId || !role) {
    throw new Error('clientId and role are required');
  }

  if (role !== ROLES.KIOSK && role !== ROLES.MONITOR) {
    throw new Error('Invalid role. Must be KIOSK or MONITOR');
  }

  const token = jwt.sign(
    {
      clientId,
      role,
      iat: Math.floor(Date.now() / 1000)
    },
    JWT_SECRET,
    { expiresIn }
  );

  logInfo('Auth', 'Token generated', {
    clientId,
    role,
    expiresIn
  });

  return token;
};
